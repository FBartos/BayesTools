  }

  formula_design <- attr(fit, "formula_design")
  formula_expected <- isTRUE(row$formulas) ||
    isTRUE(row$multi_formula) ||
    isTRUE(row$random_effects)

  if (formula_expected) {
    testthat::expect_type(formula_design, "list")
    testthat::expect_true(all(nzchar(names(formula_design))))
    testthat::expect_true(
      all(vapply(formula_design, inherits, logical(1), "BayesTools_formula_design"))
    )

    formula_parameters <- unique(unlist(lapply(prior_list, attr, which = "parameter")))
    formula_parameters <- formula_parameters[!is.na(formula_parameters)]
    testthat::expect_true(all(formula_parameters %in% names(formula_design)))
  } else {
    testthat::expect_null(formula_design)
  }

  formula_scale <- attr(fit, "formula_scale")
  if (!is.null(formula_scale)) {
    testthat::expect_type(formula_scale, "list")
    testthat::expect_true(all(names(formula_scale) %in% names(formula_design)))
    for (parameter in names(formula_scale)) {
      testthat::expect_type(formula_scale[[parameter]], "list")
      for (term in names(formula_scale[[parameter]])) {
        scale_term <- formula_scale[[parameter]][[term]]
        testthat::expect_equal(names(scale_term), c("mean", "sd"))
        testthat::expect_true(is.finite(scale_term$mean))
        testthat::expect_true(is.finite(scale_term$sd))
        testthat::expect_gt(scale_term$sd, 0)
      }
    }
  }

  expected_formula_parameters <- row$expected_formula_parameters[[1]]
  if (length(expected_formula_parameters) > 0L) {
    testthat::expect_setequal(names(formula_design), expected_formula_parameters)

    expected_formula_map <- .bayestools_catalog_formula_map(row$formula, expected_formula_parameters)
    testthat::expect_setequal(names(expected_formula_map), expected_formula_parameters)
    for (parameter in names(expected_formula_map)) {
      testthat::expect_true(parameter %in% names(formula_design))
      testthat::expect_equal(
        as.character(formula_design[[parameter]]$formula),
        as.character(expected_formula_map[[parameter]]),
        info = paste("catalog formula:", row$model_name, parameter)
      )
    }
  }

  expected_formula_scale <- row$expected_formula_scale[[1]]
  if (length(expected_formula_scale) > 0L) {
    expected_scale <- .bayestools_expected_formula_scale_by_parameter(expected_formula_scale)
    testthat::expect_false(is.null(formula_scale))
    for (parameter in names(expected_scale)) {
      testthat::expect_true(parameter %in% names(formula_scale))
      testthat::expect_setequal(names(formula_scale[[parameter]]), expected_scale[[parameter]])
    }
  }

  invisible(TRUE)
}

expect_fit_sample_dimensions <- function(fit, expected_chains = NULL, expected_iterations = NULL) {
  samples <- .bayestools_fit_mcmc_list(fit)

  if (!is.null(expected_chains) && !is.na(expected_chains)) {
    testthat::expect_equal(length(samples), expected_chains)
  }
  if (!is.null(expected_iterations) && !is.na(expected_iterations)) {
    iterations <- vapply(samples, nrow, integer(1))
    testthat::expect_equal(iterations, rep(expected_iterations, length(samples)))
  }

  invisible(samples)
}
