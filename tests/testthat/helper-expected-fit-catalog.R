.bayestools_expected_formula_scale_by_parameter <- function(expected_formula_scale,
                                                            default_parameter = "mu") {
  if (length(expected_formula_scale) == 0L) {
    return(list())
  }

  scale_names <- names(expected_formula_scale)
  if (is.null(scale_names) || all(!nzchar(scale_names))) {
    out <- list(unname(expected_formula_scale))
    names(out) <- default_parameter
    return(out)
  }

  if (any(is.na(scale_names) | !nzchar(scale_names))) {
    stop("Named expected_formula_scale entries must all declare a formula parameter.", call. = FALSE)
  }

  split(unname(expected_formula_scale), scale_names)
}

.bayestools_catalog_formula_map <- function(formula_text, expected_parameters) {
  if (length(expected_parameters) == 0L || is.na(formula_text) || !nzchar(formula_text)) {
    return(list())
  }

  parse_formula <- function(text) {
    formula <- stats::as.formula(trimws(text), env = emptyenv())
    formula <- BayesTools:::.remove_response(formula)
    formula <- BayesTools:::.remove_expressions(formula)
    formula <- BayesTools:::.remove_random_effects(formula)
    if (attr(stats::terms(formula), "intercept") == 0) {
      formula <- formula_add_intercept(formula)
    }
    environment(formula) <- emptyenv()
    formula
  }

  parts <- strsplit(formula_text, "\\s*;\\s*")[[1]]
  parts <- parts[nzchar(parts)]
  if (length(parts) == 1L && !grepl("^[[:alnum:]_.]+\\s*:", parts)) {
    out <- list(parse_formula(parts))
    names(out) <- expected_parameters[[1]]
    return(out)
  }

  out <- list()
  for (part in parts) {
    match <- regexec("^([[:alnum:]_.]+)\\s*:\\s*(.+)$", part)
    pieces <- regmatches(part, match)[[1]]
    if (length(pieces) != 3L) {
      stop("Could not parse catalog formula declaration: ", part, call. = FALSE)
    }
    out[[pieces[[2]]]] <- parse_formula(pieces[[3]])
  }
  out[names(out) %in% expected_parameters]
}

