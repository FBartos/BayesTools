  as.vector(outer(
    seq_len(n_groups),
    seq_len(n_columns),
    function(group, column) paste0(stem, "[", group, ",", column, "]")
  ))
}

.bayestools_find_calls <- function(expr, fun) {
  matches <- list()

  walk <- function(x) {
    if (is.expression(x)) {
      for (item in as.list(x)) {
        walk(item)
      }
    } else if (is.call(x)) {
      head <- x[[1L]]
      if (is.symbol(head) && identical(as.character(head), fun)) {
        matches[[length(matches) + 1L]] <<- x
      }
      for (item in as.list(x)[-1L]) {
        walk(item)
      }
    }
  }

  walk(expr)
  matches
}

.bayestools_static_string <- function(expr, default = NA_character_) {
  if (is.character(expr) && length(expr) == 1L) {
    return(expr)
  }
  default
}

.bayestools_static_logical <- function(expr, default = FALSE) {
  if (is.logical(expr) && length(expr) == 1L && !is.na(expr)) {
    return(expr)
  }
  default
}

.bayestools_save_fit_catalog_rows <- function(source_file = bayestools_model_fit_source_file()) {
  if (!file.exists(source_file)) {
    stop("Model fit source file not found: ", source_file, call. = FALSE)
  }

  flag_cols <- bayestools_registry_flag_columns()
  calls <- .bayestools_find_calls(parse(source_file), "save_fit")

  rows <- lapply(calls, function(call) {
    args <- as.list(call)[-1L]
    arg_names <- names(args)
    if (is.null(arg_names)) {
      arg_names <- rep("", length(args))
    }

    get_arg <- function(name, position = NULL, default = NULL) {
      named_index <- match(name, arg_names)
      if (!is.na(named_index)) {
        return(args[[named_index]])
      }
      if (!is.null(position) && length(args) >= position && !nzchar(arg_names[[position]])) {
        return(args[[position]])
      }
      default
    }

    model_name <- .bayestools_static_string(get_arg("name", position = 2L))
    note <- .bayestools_static_string(get_arg("note"), default = "")

    flags <- vapply(flag_cols, function(flag) {
      .bayestools_static_logical(get_arg(flag), default = FALSE)
    }, logical(1))

    has_marglik <- !is.null(get_arg("marglik"))
    prior_features <- names(flags)[flags]
    if (length(prior_features) == 0L) {
      prior_features <- "none"
    }

    data.frame(
      model_name = model_name,
      profile = "fixture",
      fit_file = paste0(model_name, ".RDS"),
      marglik_file = if (has_marglik) paste0(model_name, ".RDS") else NA_character_,
      has_marglik = has_marglik,
      as.data.frame(as.list(flags), check.names = FALSE),
      model_family = "generated-fixture",
      formula = NA_character_,
      scale_policy = if (flags[["formulas"]]) "catalog-unverified" else "none",
      prior_features = paste(prior_features, collapse = ","),
      requires_package = NA_character_,
      oracle_type = "registry-metadata",
      expected_chains = NA_integer_,
      expected_iterations = NA_integer_,
      expected_parameter_count = NA_integer_,
      tolerance = NA_real_,
      note = note,
      stringsAsFactors = FALSE,
      check.names = FALSE
    )
  })

  catalog <- do.call(rbind, rows)
  optional <- bayestools_optional_fit_requirements()
  optional_index <- match(optional$model_name, catalog$model_name)
  if (anyNA(optional_index)) {
    stop(
      "Optional fit catalog entries are not generated by test-00-model-fits.R: ",
      paste(optional$model_name[is.na(optional_index)], collapse = ", "),
      call. = FALSE
    )
  }
  catalog$requires_package[optional_index] <- optional$requires_package

  catalog$expected_monitor <- I(rep(list(character()), nrow(catalog)))
  catalog$expected_formula_parameters <- I(rep(list(character()), nrow(catalog)))
  catalog$expected_formula_scale <- I(rep(list(character()), nrow(catalog)))
  rownames(catalog) <- NULL
  catalog
}

