bayestools_expected_fit_catalog <- function() {
  catalog <- .bayestools_save_fit_catalog_rows()
  overrides <- bayestools_semantic_fit_catalog_overrides()

  missing_overrides <- setdiff(overrides$model_name, catalog$model_name)
  if (length(missing_overrides) > 0L) {
    stop(
      "Semantic fit catalog overrides are not generated by test-00-model-fits.R: ",
      paste(missing_overrides, collapse = ", "),
      call. = FALSE
    )
  }

  for (model_name in overrides$model_name) {
    catalog_index <- match(model_name, catalog$model_name)
    override_index <- match(model_name, overrides$model_name)

    for (column in setdiff(names(overrides), "note")) {
      catalog[[column]][catalog_index] <- overrides[[column]][override_index]
    }
  }

  rownames(catalog) <- NULL
  catalog
}

